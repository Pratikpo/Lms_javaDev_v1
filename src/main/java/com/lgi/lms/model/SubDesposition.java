package com.lgi.lms.model;

public class SubDesposition {
    private Long id;
    private Long despositionId;
    private String label;
    
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getDespositionId() {
		return despositionId;
	}
	public void setDespositionId(Long despositionId) {
		this.despositionId = despositionId;
	}
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}

   
}
